package LogicalPrograms;

public class ArithmeticFunctions {
public static void main(String[] args) {
	int i=10;
	int result=1;
	int b=i--;
	int c=-i;
	result *=i;
	System.out.println("i++ is "+i++);
	System.out.println("++i is "+ ++i);
	System.out.println("i-- is "+b);
	System.out.println("-i is "+c);
	System.out.println(result);
	
}
}
